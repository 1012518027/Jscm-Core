/**
 * 边大小
 */
public class TagBorderSize {
    public int leftSize;
    public int topSize;
    public int rightSize;
    public int bottomSize;

    @Override
    public String toString() {
        return "TagBorderSize{" +
                "leftSize=" + leftSize +
                ", topSize=" + topSize +
                ", rightSize=" + rightSize +
                ", bottomSize=" + bottomSize +
                '}';
    }
}
